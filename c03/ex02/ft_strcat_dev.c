{
	int	i;
	int	j;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	j = 0;
	while (src[j] != '\0')
	{
		dest[j+i] = src [j];
		j++;
	}
	dest[i+j] = '\0';
	return (dest);
}

