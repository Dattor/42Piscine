#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;

	i = 1;
	argv++;
	while (i < argc)
	{
		while (**argv != '\0')
		{
			write (1, (*argv)++, 1);
		}
		write (1, "\n", 1);
		argv++;
		i++;
	}
}
